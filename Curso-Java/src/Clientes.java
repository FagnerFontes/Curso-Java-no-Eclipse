public class Clientes {

    String nomeCliente; 
    int numeroConta; 
    double saldoAtual; 
    double novoSaldo; 
    
   
    

    public void cliente(String nomeCliente, int numeroConta, double saldoAtual){
         this.nomeCliente = nomeCliente;
         this.numeroConta = numeroConta;
         this.saldoAtual = saldoAtual; 
         this.novoSaldo = novoSaldo; 
    }  
}
